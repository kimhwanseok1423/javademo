package java002_statements.prob;

public class Java026_for {

	public static void main(String[] args) {
		int sum=0;
		int i;
		
		for(i=1;;i++) {
			sum=sum+i;
			System.out.println("i="+i +"sum="+sum);
			if(sum>=15) {
				break;
				
					
			}
		}
		
		// i=1 sum1   i=2   sum2
		
		
		
		
	}

}
